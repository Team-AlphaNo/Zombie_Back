package com.teamalphano.zombieboom.model.character;

public class CharacterData {
    private int charNo;
    private int itemNo;
    private String itemName;
    private String itemImgKey;
    private int spineModelKey;
    private int costumeKey;
    private String colorEyes;
    private String colorBody;
    private String colorEtc;

    public int getCharNo() {
        return charNo;
    }

    public void setCharNo(int charNo) {
        this.charNo = charNo;
    }

    public int getItemNo() {
        return itemNo;
    }

    public void setItemNo(int itemNo) {
        this.itemNo = itemNo;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getItemImgKey() {
        return itemImgKey;
    }

    public void setItemImgKey(String itemImgKey) {
        this.itemImgKey = itemImgKey;
    }

    public int getSpineModelKey() {
        return spineModelKey;
    }

    public void setSpineModelKey(int spineModelKey) {
        this.spineModelKey = spineModelKey;
    }

    public int getCostumeKey() {
        return costumeKey;
    }

    public void setCostumeKey(int costumeKey) {
        this.costumeKey = costumeKey;
    }

    public String getColorEyes() {
        return colorEyes;
    }

    public void setColorEyes(String colorEyes) {
        this.colorEyes = colorEyes;
    }

    public String getColorBody() {
        return colorBody;
    }

    public void setColorBody(String colorBody) {
        this.colorBody = colorBody;
    }

    public String getColorEtc() {
        return colorEtc;
    }

    public void setColorEtc(String colorEtc) {
        this.colorEtc = colorEtc;
    }
}
